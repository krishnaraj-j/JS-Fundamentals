// Relational operators
var lesser_than = 5 < 10
var greater_than = 5 > 10
var lesser_than_or_equals = 5 <= 10
var greater_than_or_equals = 5 >= 10
console.log(lesser_than, greater_than, lesser_than_or_equals, greater_than_or_equals)

// Equality operators
var x = 1
console.log(x == '1') // lose comparison
console.log(x === '1') // strict comparison